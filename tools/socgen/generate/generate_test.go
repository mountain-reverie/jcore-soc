// minimalRes is a Resolution rich enough that the emitters return without
// panicking (empty device/signal sets -> minimal but valid VHDL).
func minimalRes() *elaborate.Resolution {
	return &elaborate.Resolution{}
}

// minimalLib returns an iface.Library with a cfg_clk_cpu_period_ns constant
// (20 ns -> 50 MHz). Required because devicetree.BoardDTS calls cpuFreq(b.Library)
// which returns an error when the constant is absent, causing Build to fail even
// in the best-effort path.
func minimalLib() *iface.Library {
	return &iface.Library{
		Entities: map[string]*iface.Entity{},
		Packages: map[string]*iface.Package{
			"cfg": {
				Name: "cfg",
				Constants: []*iface.Constant{
					{
						Name:  "CFG_CLK_CPU_PERIOD_NS",
						Value: &vhdl.BasicLit{Kind: vhdl.INT, Value: "20"},
					},
				},
			},
		},
		Architectures:  map[string][]*iface.Architecture{},
		Configurations: map[string]*iface.Configuration{},
	}
}

func boardWith(plugins []string) *board.Board {
	return &board.Board{
		Name:    "synthetic",
		Design:  &design.Design{Plugins: plugins},
		Library: minimalLib(),
	}
}

func names(files []File) map[string]bool {
	m := make(map[string]bool, len(files))
	for _, f := range files {
		m[f.Name] = true
	}
	return m
}

func TestBuildSelectsBoardHOnly(t *testing.T) {
	files, err := Build(boardWith([]string{"board.h"}), minimalRes())
	if err != nil {
		t.Fatalf("Build: %v", err)
	}
	n := names(files)
	if !n["board.h"] {
		t.Errorf("expected board.h in file set: %v", n)
	}
	if n["board.dts"] {
		t.Errorf("did not expect board.dts when device_tree absent: %v", n)
	}
	for _, core := range []string{"devices.vhd", "soc.vhd", "pad_ring.vhd", "build.mk"} {
		if !n[core] {
			t.Errorf("missing core file %q: %v", core, n)
		}
	}
}

func TestBuildSelectsDeviceTreeOnly(t *testing.T) {
	files, err := Build(boardWith([]string{"device_tree"}), minimalRes())
	if err != nil {
		t.Fatalf("Build: %v", err)
	}
	n := names(files)
	if !n["board.dts"] {
		t.Errorf("expected board.dts: %v", n)
	}
	if n["board.h"] {
		t.Errorf("did not expect board.h when board.h plugin absent: %v", n)
	}
}

func TestBuildDuplicatePlugin(t *testing.T) {
	_, err := Build(boardWith([]string{"board.h", "board.h"}), minimalRes())
	if !errors.Is(err, ErrDuplicatePlugin) {
		t.Errorf("expected ErrDuplicatePlugin, got %v", err)
	}
}

func TestBuildUnknownPlugin(t *testing.T) {
	_, err := Build(boardWith([]string{"bogus"}), minimalRes())
	if !errors.Is(err, ErrUnknownPlugin) {
		t.Errorf("expected ErrUnknownPlugin, got %v", err)
	}
}

func TestBuildMKListsOnlyVHDL(t *testing.T) {
	files, err := Build(boardWith([]string{"device_tree", "board.h"}), minimalRes())
	if err != nil {
		t.Fatalf("Build: %v", err)
	}
	var mk string
	for _, f := range files {
		if f.Name == "build.mk" {
			mk = f.Content
		}
	}
	want := "# This file is generated by soc_gen and will be overwritten next time\n" +
		"# the tool is run. See soc_top/README for information on running soc_gen.\n" +
		"$(VHDLS) += devices.vhd\n$(VHDLS) += soc.vhd\n$(VHDLS) += pad_ring.vhd\n"
	if mk != want {
		t.Errorf("build.mk content:\n got %q\nwant %q", mk, want)
	}
	if last := files[len(files)-1].Name; last != "build.mk" {
		t.Errorf("build.mk should be appended last, got last file %q", last)
	}
}

func TestBuildMimasV2(t *testing.T) {
	root := os.Getenv("JCORE_SOC_ROOT")
	if root == "" {
		t.Skip("JCORE_SOC_ROOT not set")
	}
	b, lerr := board.Load(root, "mimas_v2")
	if b == nil || b.Design == nil {
		t.Fatalf("board.Load: %v", lerr)
	}
	res, rerr := elaborate.Elaborate(b)
	if rerr != nil {
		t.Logf("elaborate notes: %v", rerr)
	}
	files, err := Build(b, res)
	if err != nil {
		t.Fatalf("Build: %v", err)
	}

	// 1. file set matches the golden set exactly.
	var got []string
	byName := map[string]string{}
	for _, f := range files {
		got = append(got, f.Name)
		byName[f.Name] = f.Content
	}
	sort.Strings(got)
	want := []string{"board.dts", "board.h", "build.mk", "devices.vhd", "pad_ring.vhd", "soc.vhd"}
	if len(got) != len(want) {
		t.Fatalf("file set = %v, want %v", got, want)
	}
	for i := range want {
		if got[i] != want[i] {
			t.Errorf("file set[%d] = %q, want %q (full: %v)", i, got[i], want[i], got)
		}
	}

	// 2. build.mk byte-exact vs golden.
	golden, gerr := os.ReadFile(filepath.Join(root, "targets/boards/mimas_v2/build.mk"))
	if gerr != nil {
		t.Fatalf("read golden build.mk: %v", gerr)
	}
	if byName["build.mk"] != string(golden) {
		t.Errorf("build.mk not byte-exact:\n got %q\nwant %q", byName["build.mk"], string(golden))
	}

	// 3. each file equals its direct emitter call (Build = selection + assembly).
	devVHD, _ := emit.Devices(res)
	socVHD, _ := emit.SoC(res)
	padVHD, _ := emit.PadRing(res)
	dts, _ := devicetree.BoardDTS(b, res)
	boardH, _ := cheader.BoardH(res)
	for _, c := range []struct{ name, want string }{
		{"devices.vhd", devVHD}, {"soc.vhd", socVHD}, {"pad_ring.vhd", padVHD},
		{"board.dts", dts}, {"board.h", boardH},
	} {
		if byName[c.name] != c.want {
			t.Errorf("%s content differs from its direct emitter call", c.name)
		}
	}

	// 4. board.h byte-exact vs golden (cheap extra assurance).
	bh, bherr := os.ReadFile(filepath.Join(root, "targets/boards/mimas_v2/board.h"))
	if bherr != nil {
		t.Fatalf("read golden board.h: %v", bherr)
	}
	if byName["board.h"] != string(bh) {
		t.Errorf("board.h not byte-exact vs golden")
	}
}
